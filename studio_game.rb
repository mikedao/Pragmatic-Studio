name = 'larry'

health = 60

puts "#{name}'s health is #{health}"

# triple the health
puts "#{name}'s health is #{health*3}"

# divide by nine integer
puts "#{name}'s health is #{health/9}"

# divide by nine decimal
puts "#{name}'s health is #{health/9.0}"

# printing out a list of players formatted
puts "\nPlayers: \n"
puts "\tlarry"
puts "\tcurly"
puts "\tmoe"

